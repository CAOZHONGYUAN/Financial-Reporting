package com.fms.entity;

public class Statistics {
    private Integer PropertyCount;
    private Integer TrafficCount;
    private Integer TravelCount;
    private Integer WelfareCount;

    public Integer getPropertyCount() {
        return PropertyCount;
    }

    public void setPropertyCount(Integer propertyCount) {
        PropertyCount = propertyCount;
    }

    public Integer getTrafficCount() {
        return TrafficCount;
    }

    public void setTrafficCount(Integer trafficCount) {
        TrafficCount = trafficCount;
    }

    public Integer getTravelCount() {
        return TravelCount;
    }

    public void setTravelCount(Integer travelCount) {
        TravelCount = travelCount;
    }

    public Integer getWelfareCount() {
        return WelfareCount;
    }

    public void setWelfareCount(Integer welfareCount) {
        WelfareCount = welfareCount;
    }
}
